def create_upos(line, prompt_text):

    # Export documents into plain text and return in list
    doc = nlp(line)
    s = ""
    for sentence in doc.sentences:
        previous_ner = False
        for token in sentence.tokens:
            if token.ner != "O" and token.text not in prompt_text:
                #if not previous_ner:
                s += '<UNK>[[[' + token.text + ']]] '
                previous_ner = True
            else:
                s += token.text + ' '
                previous_ner = False
    return s


